# Arithmentic Operator samples
print("5 + 2=", 5+2)    #addition 
print("5 - 2=", 5-2)    #subtraction
print("5 * 2=", 5*2)    #multiplication
print("5 / 2=", 5/2)    #division
print("5 % 2=", 5%2)    #modulus
print("5 ** 2=", 5**2)  #exponential
print("7 // 2=", 7//2)  #floor division

